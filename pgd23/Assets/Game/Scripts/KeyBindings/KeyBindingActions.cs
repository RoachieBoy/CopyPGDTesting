        /// </summary>
        CrouchKey,
        /// <summary>
        ///     Key for walking to the left
        /// </summary>
        WalkLeftKey,
        /// <summary>
        ///     Key for walking to the right
        /// </summary>
        WalkRightKey,
        /// <summary>
        ///     Key for dashing the player 
        /// </summary>
        DashKey,
    }
}